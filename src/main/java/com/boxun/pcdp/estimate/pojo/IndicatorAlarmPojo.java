package com.boxun.pcdp.estimate.pojo;

public class IndicatorAlarmPojo {

	private Long id;
	private String name;
	private Double alarm;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getAlarm() {
		return alarm;
	}
	public void setAlarm(Double alarm) {
		this.alarm = alarm;
	}
}
