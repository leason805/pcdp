package com.boxun.pcdp.capacity.entity;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

@Entity
@Table(name = "CA_QUESTION")
public class CEvaluateQuestion implements Serializable{

	private static final long serialVersionUID = 6178303284483189811L;

	@Id 
	@GeneratedValue(strategy=GenerationType.IDENTITY)
	@Column(name = "ID", unique = true, nullable = false, insertable = true, updatable = true)
	private Long id;
	
	@Column(name = "TITLE")
	private String title;

	@Column(name = "DESCRIPTION", length=5000)
	private String description;
	
	@Column(name = "QUESTION", length=5000)
	private String question;
	
	@ManyToOne//(fetch = FetchType.LAZY)
	@JoinColumn(name = "PROJECT_ID")
	private CEvaluateProject project;
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getQuestion() {
		return question;
	}

	public void setQuestion(String question) {
		this.question = question;
	}

	public CEvaluateProject getProject() {
		return project;
	}

	public void setProject(CEvaluateProject project) {
		this.project = project;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}
